#include "include/libft.h"

int		ft_deltabi2(int ***tab, int size)
{
	int i;

	i = 0;
	while (i < size)
	{
		free((*tab)[i]);
		(*tab)[i++] = NULL;
	}
	free(*tab);
	*tab = NULL;
	return (0);
}
