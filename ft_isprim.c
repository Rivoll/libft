#include "include/libft.h"

size_t		ft_isprim(size_t nb)
{
	size_t		i;

	i = 2;
	if (nb < 2)
		return (0);
	while (2 * i <= nb)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
