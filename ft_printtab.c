#include "include/libft.h"

void	ft_printtab(char **tab)
{
	int i;

	i = 0;
	while (tab[i])
	{
		ft_putnbr(i);
		ft_putstr(": ");
		ft_putstr(tab[i++]);
		ft_putchar('\n');
	}
}
