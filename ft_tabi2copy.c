#include "include/libft.h"

int		ft_tabi2copy(int **tab1, int **tab2, int x, int y)
{
	int i;

	i = 0;
	while (i < y)
	{
		ft_tabicopy(tab1[i], tab2[i], x);
		i++;
	}
	return (0);
}
